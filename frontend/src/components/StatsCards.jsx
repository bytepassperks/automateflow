export default function StatsCards({ stats }) {
  const cards = [
    { label: 'Total Jobs', value: stats.total, icon: TotalIcon, color: 'text-primary-400' },
    { label: 'Completed', value: stats.completed, icon: CompletedIcon, color: 'text-green-400' },
    { label: 'Failed', value: stats.failed, icon: FailedIcon, color: 'text-red-400' },
    { label: 'Running', value: stats.running, icon: RunningIcon, color: 'text-blue-400' },
    { label: 'Success Rate', value: stats.total > 0 ? `${Math.round((stats.completed / stats.total) * 100)}%` : '0%', icon: RateIcon, color: 'text-violet-400' },
  ];

  return (
    <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-5 gap-4">
      {cards.map((card) => (
        <div key={card.label} className="bg-gray-900 border border-gray-800 rounded-xl p-5">
          <div className="flex items-center justify-between mb-3">
            <card.icon className={`w-5 h-5 ${card.color}`} />
          </div>
          <p className="text-2xl font-bold text-white">{card.value}</p>
          <p className="text-sm text-gray-400 mt-1">{card.label}</p>
        </div>
      ))}
    </div>
  );
}

